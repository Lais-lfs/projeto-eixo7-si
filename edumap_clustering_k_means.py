"""### **Análise Avançada de Silhueta (_Silhouette Diagram_)**"""

from sklearn.metrics import silhouette_samples
import matplotlib.cm as cm

# Amostragem para plotagem eficiente
np.random.seed(42)
indices_amostra = np.random.choice(len(X_scaled), 5000, replace=False)
X_amostra = X_scaled[indices_amostra]
labels_amostra = clusters[indices_amostra]

# Calculando os scores de silhueta para cada amostra individual
sample_silhouette_values = silhouette_samples(X_amostra, labels_amostra)
silhouette_avg = silhouette_score(X_amostra, labels_amostra)

fig, ax = plt.subplots(figsize=(8, 6))
y_lower = 10

for i in range(2): # Loop para os 2 clusters (0 e 1)
    ith_cluster_silhouette_values = sample_silhouette_values[labels_amostra == i]
    ith_cluster_silhouette_values.sort()

    size_cluster_i = ith_cluster_silhouette_values.shape[0]
    y_upper = y_lower + size_cluster_i

    color = cm.coolwarm(float(i) / 2)
    ax.fill_betweenx(np.arange(y_lower, y_upper), 0, ith_cluster_silhouette_values,
                     facecolor=color, edgecolor=color, alpha=0.7)

    ax.text(-0.05, y_lower + 0.5 * size_cluster_i, str(i))
    y_lower = y_upper + 10

ax.set_title("Gráfico de Silhueta por Amostra (Amostra de 5k registros)")
ax.set_xlabel("Coeficiente de Silhueta")
ax.set_ylabel("ID do Cluster")

# Linha vertical indicando o Score de Silhueta Médio
ax.axvline(x=silhouette_avg, color="red", linestyle="--", label=f'Média: {silhouette_avg:.3f}')
ax.set_yticks([])
ax.set_xlim([-0.1, 1])
ax.legend()
plt.grid(True, alpha=0.3)
plt.show()